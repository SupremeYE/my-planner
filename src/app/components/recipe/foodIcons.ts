// 정규화: 소문자 + 공백 제거. 사전 키도 동일 규칙으로 정규화해서 저장.
function normalize(s: string): string {
  return s.toLowerCase().replace(/\s+/g, '').trim();
}

// 사전 — 정규화 키, 긴 키부터(부분일치 시 더 구체적 매칭 우선).
const DICT: Array<[string, string]> = RAW_ENTRIES
  .map(([k, e]) => [normalize(k), e] as [string, string])
  .sort((a, b) => b[0].length - a[0].length);

// 카테고리 기본 아이콘 (매칭 실패 폴백)
const CATEGORY_FALLBACK: Record<FridgeCategory, string> = {
  냉장: '🧊',
  냉동: '❄️',
  실온: '🫙',
};

const GENERIC_FALLBACK = '🥄';

/**
 * 음식 이름(과 선택적 카테고리)으로 표시용 아이콘 문자열을 반환.
 * 호출부는 시그니처만 의존 — 내부 사전이나 표현 방식(이모지/SVG)이 바뀌어도 안전.
 */
export function getFoodIcon(name: string | null | undefined, category?: FridgeCategory): string {
  const n = normalize(name ?? '');
  if (n) {
    // 1) 정확히 같은 키 우선
    for (const [k, emo] of DICT) if (k === n) return emo;
    // 2) 부분일치(이름이 키를 포함 또는 키가 이름을 포함)
    for (const [k, emo] of DICT) if (n.includes(k) || k.includes(n)) return emo;
  }
  if (category && CATEGORY_FALLBACK[category]) return CATEGORY_FALLBACK[category];
  return GENERIC_FALLBACK;
}

